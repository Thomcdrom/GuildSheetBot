export class Day {
    private day :string;
    private row :string;

    constructor() {}

    public getDay() :string {
        return this.day;
    }

    public setDay(day :string) {
        this.day = day;
    }

    public getRow() :string {
        return this.row;
    }

    public setRow(row :string) {
        this.row = row;
    }
}